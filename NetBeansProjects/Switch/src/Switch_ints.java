
public class Switch_ints {

    public static void main(String[] args) {
        int x = 3;
        switch (x) {
            case 1:
                System.out.println("fue un uno");
                break;
            case 2:
                System.out.println("fue un dos");
                break;
            default:
                System.out.println("fue otra cosa");

        }
    }
}
